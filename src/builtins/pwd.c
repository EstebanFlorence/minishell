#include "minishell.h"

int	ft_pwd(int n)
{
	char	*pwd;

	(void)n;
	pwd = getcwd(NULL, 0);
	if (pwd == NULL)
		return (1);
	ft_printf("%s\n", pwd);
	free(pwd);
	return (0);
}
